#ifndef _SIMPLE_RISC_TYPES_H_
#define _SIMPLE_RISC_TYPES_H_

#include <stdint.h>

typedef int32_t word;

#define WORD_LEN 32

#define REG_COUNT 16
#define RA 15                   // number of "Return Address" register
#define SP 14                   // number of "Stack Pointer" register
#define MEM_LEN (1024)

#define OPCODE_SHIFT 27
#define DST_REG_SHIFT 22
#define SRC_REG_1_SHIFT 18
#define SRC_REG_2_SHIFT 14
#define OPCODE_LEN 5
#define MAX_OFFSET ((1 << 28) - 1)
#define OPCODE_MASK                 0b11111000000000000000000000000000
#define OPCODE_AND_I_BIT_MASK       0b11111100000000000000000000000000
#define I_BIT_MASK                  0b00000100000000000000000000000000
#define ONE_ARG_MASK                0b00000111111111111111111111111111
#define MODIFIERS_BITS_MASK         0b00000000000000110000000000000000
#define MODIFIERS_BITS_DEFAULT_MASK 0b00000000000000000000000000000000
#define MODIFIERS_BITS_U_MASK       0b00000000000000010000000000000000
#define MODIFIERS_BITS_H_MASK       0b00000000000000100000000000000000
#define DST_REG_MASK                0b00000011110000000000000000000000
#define SRC_REG_1_MASK              0b00000000001111000000000000000000
#define SRC_REG_2_MASK              0b00000000000000111100000000000000
#define IMMEDIATE_3_MASK            0b00000000000000001111111111111111

#define ADD_OPCODE  0
#define SUB_OPCODE  (1  << OPCODE_SHIFT)
#define MUL_OPCODE  (2  << OPCODE_SHIFT)
#define DIV_OPCODE  (3  << OPCODE_SHIFT)
#define MOD_OPCODE  (4  << OPCODE_SHIFT)
#define CMP_OPCODE  (5  << OPCODE_SHIFT)
#define AND_OPCODE  (6  << OPCODE_SHIFT)
#define OR_OPCODE   (7  << OPCODE_SHIFT)
#define NOT_OPCODE  (8  << OPCODE_SHIFT)
#define MOV_OPCODE  (9  << OPCODE_SHIFT)
#define LSL_OPCODE  (10 << OPCODE_SHIFT)
#define LSR_OPCODE  (11 << OPCODE_SHIFT)
#define ASR_OPCODE  (12 << OPCODE_SHIFT)
#define NOP_OPCODE  (13 << OPCODE_SHIFT)
#define LD_OPCODE   (14 << OPCODE_SHIFT)
#define ST_OPCODE   (15 << OPCODE_SHIFT)
#define BEQ_OPCODE  (16 << OPCODE_SHIFT)
#define BGT_OPCODE  (17 << OPCODE_SHIFT)
#define B_OPCODE    (18 << OPCODE_SHIFT)
#define CALL_OPCODE (19 << OPCODE_SHIFT)
#define RET_OPCODE  (20 << OPCODE_SHIFT)
#define END_PROGRAM (31 << OPCODE_SHIFT)

#endif
